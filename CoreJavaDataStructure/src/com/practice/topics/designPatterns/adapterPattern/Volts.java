package com.practice.topics.designPatterns.adapterPattern;

public class Volts {
	private int volts;

	public Volts(int volts) {
		super();
		this.volts = volts;
	}

	public Volts() {
	}

	public int getVolts() {
		return volts;
	}

	public void setVolts(int volts) {
		this.volts = volts;
	}
	
		

}
